package uk.co.thefishlive.auth.assessments.questions;

/**
 * A enum describing the different question types possible.
 */
public enum QuestionType {

    /**
     * A simple multichoice question with a given number of options.
     */
    MULTI_CHOICE,

    /**
     * A short answer question with a given numerical or algebraic answer.
     */
    ANSWER;

}
